        context: context,
        phoneNumber: phoneNumberVal,
        onCodeSent: (context) async {
          context.goNamedAuth(
            'sms_confirmation',
            context.mounted,
            ignoreRedirect: true,
          );
        },
      );
    });

    authManager.handlePhoneAuthStateChanges(context);
    _model.textController ??= TextEditingController();
    _model.textFieldFocusNode ??= FocusNode();

